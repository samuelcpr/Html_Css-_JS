// Manipulando Strings e Números


// Transforme letras minúsculas em maiúsculas. Faça o
// contrario disso também

let word = "Programa é muito bacana!"
// esse com toUpperCase transforma todas as letras em maiusculo
console.log(word.toUpperCase())

let keize = "PROGRAMAR É BACANA"
// esse com toUpperCase transforma todas as letras em minusculas
console.log(keize.toUpperCase())
// Maiuscula e minusculas
console.log(keize.toUpperCase().toUpperCase)